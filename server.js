// AI idle chatter — called periodically during explore phase
function aiIdleChat(game, pIdx) {
  const charIdx = game.characterAssignments[pIdx];
  const phase = game.phase;

  // Only 15% chance per idle tick to avoid spamming
  if (Math.random() >= 0.15) return;

  // Dynamically generate an idle observation
  let line = generateAILine(game, charIdx, pIdx, { type: 'idle' });
  if (!line) return;

  // Ensure uniqueness
  const used = game.aiUsedLinesGlobal || new Set();
  if (used.has(line)) {
    // Try a discussion observation instead
    line = generateAILine(game, charIdx, pIdx, { type: 'discuss' });
    if (!line || used.has(line)) return;
  }

  // Pre-claim and say
  if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
  game.aiUsedLinesGlobal.add(line);
  aiSay(game, pIdx, line);
}

// Enhanced AI discussion — richer contextual behavior
function aiDiscussEnhanced(game, pIdx) {
  if (game.phase !== 'discuss') return;
  const player = game.players[pIdx];
  if (!player || !player.isAI) return;

  const charIdx = game.characterAssignments[pIdx];
  const isMurderer = charIdx === 0;

  // Helper: generate a dynamic line, check for uniqueness, pre-claim and say it
  function sayDynamic(context) {
    let line = generateAILine(game, charIdx, pIdx, context);
    if (!line) return;
    // Ensure uniqueness — if already said, try once more with a different context
    const used = game.aiUsedLinesGlobal || new Set();
    if (used.has(line)) {
      // Try generating a reaction to recent chat instead
      line = generateAILine(game, charIdx, pIdx, { type: 'react' });
      if (!line || used.has(line)) return;
    }
    if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
    game.aiUsedLinesGlobal.add(line);
    aiSay(game, pIdx, line);
  }

  // Behavior weights:
  // 25% — share a clue + dynamically generated dialogue
  // 20% — discussion observation (dynamic)
  // 15% — ask a question to another AI character (dynamic)
  // 10% — accuse a suspect (dynamic)
  // 10% — react to recent chat (dynamic)
  // 10% — idle observation (dynamic)
  // 10% — stay silent

  const roll = Math.random();

  if (roll < 0.25 && player.inventory.length > 0) {
    // Share a clue — only ones not already publicly shared
    const sharedClueKeys = game.sharedClueKeys || [];
    const unsharedClues = player.inventory.filter(k => !sharedClueKeys.includes(k));
    if (unsharedClues.length > 0) {
      const itemKey = pickRandom(unsharedClues);
      const item = ITEMS[itemKey];
      if (item) {
        const charName = CHARACTERS[charIdx].name;
        emitToRoom(game.roomCode, 'clue-shared', {
          fromCharacter: charName + ' (AI)',
          fromAvatar: CHARACTERS[charIdx].avatar,
          itemKey: itemKey,
          item: { name: item.name, desc: item.desc }
        });
        if (!game.sharedClueKeys) game.sharedClueKeys = [];
        if (!game.sharedClueKeys.includes(itemKey)) game.sharedClueKeys.push(itemKey);
        // Dynamically generate clue share dialogue
        sayDynamic({ type: 'clue_share', clueKey: itemKey });
      }
    }
  } else if (roll < 0.45) {
    // Discussion observation — dynamically generated
    sayDynamic({ type: 'discuss' });
  } else if (roll < 0.60) {
    // Ask a question to another AI character — dynamically generated
    aiAskAnotherAI(game, pIdx);
  } else if (roll < 0.70) {
    // Accuse a suspect — dynamically generated
    sayDynamic({ type: 'accuse' });
  } else if (roll < 0.80) {
    // React to recent chat — dynamically generated
    sayDynamic({ type: 'react' });
  } else if (roll < 0.90) {
    // Idle observation — dynamically generated
    sayDynamic({ type: 'idle' });
  }
  // 10% — silence
}

// AI asks a question to another AI character, who then responds
function aiAskAnotherAI(game, askerIdx) {
  const askerCharIdx = game.characterAssignments[askerIdx];
  const askerChar = CHARACTERS[askerCharIdx];
  const isMurderer = askerCharIdx === 0;

  // Pick a target AI player (not self) — the one being asked
  const otherAI = game.players
    .map((p, i) => ({ p, i }))
    .filter(({ p, i }) => p.isAI && i !== askerIdx)
    .map(({ i }) => i);

  if (otherAI.length === 0) return;

  const targetIdx = pickRandom(otherAI);
  const targetCharIdx = game.characterAssignments[targetIdx];

  // Dynamically generate the question
  let question = generateAILine(game, askerCharIdx, askerIdx, { type: 'question', targetCharIdx });
  if (!question) return;

  // Ensure uniqueness
  const used = game.aiUsedLinesGlobal || new Set();
  if (used.has(question)) return;

  // Pre-claim the question
  if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
  game.aiUsedLinesGlobal.add(question);

  // Asker says the question
  aiSay(game, askerIdx, question);

  // ALL eligible AI respond (not just the target), with the target first
  // This mirrors the player-question behavior where all AI chime in
  const eligibleAI = game.players
    .map((p, i) => ({ p, i }))
    .filter(({ p, i }) => p.isAI && i !== askerIdx && game.phase !== 'vote')
    .map(({ i }) => i);

  // Build responder order: target first, then others
  const responderOrder = [targetIdx];
  for (const aiIdx of eligibleAI) {
    if (!responderOrder.includes(aiIdx)) {
      responderOrder.push(aiIdx);
    }
  }

  // Helper: generate a unique reply, trying multiple strategies (same as player-question)
  function generateUniqueReply(aiIdx, contextTypes) {
    const charIdx = game.characterAssignments[aiIdx];
    const isMurd = charIdx === 0;
    const currentUsed = game.aiUsedLinesGlobal || new Set();

    for (const ctxType of contextTypes) {
      let line = null;
      if (ctxType === 'contextual') {
        line = generateContextualResponse(game, question, charIdx, aiIdx, isMurd);
      } else {
        line = generateAILine(game, charIdx, aiIdx, { type: ctxType });
      }
      if (line && !currentUsed.has(line)) {
        return line;
      }
    }

    // Last resort: try each shared clue as a basis for a unique reaction
    const sharedClues = game.sharedClueKeys || [];
    for (const clueKey of sharedClues) {
      const line = generateClueReaction(game, charIdx, clueKey, isMurd);
      if (line && !currentUsed.has(line)) {
        return line;
      }
    }

    // Try alibi
    const alibiLine = generateAlibi(charIdx);
    if (alibiLine && !currentUsed.has(alibiLine)) {
      return alibiLine;
    }

    // Try discussion observation
    const obsLine = generateDiscussionObservation(game, charIdx, isMurd);
    if (obsLine && !currentUsed.has(obsLine)) {
      return obsLine;
    }

    return null;
  }

  // Context priority: contextual response first, then reactions, discussion, idle
  const contextPriority = ['contextual', 'react', 'discuss', 'idle', 'accuse'];

  // Generate unique replies for all responders, with staggered delays
  let responseIndex = 0;
  responderOrder.forEach((aiIdx) => {
    const delay = (responseIndex + 1) * (2000 + Math.random() * 2000);

    setTimeout(() => {
      if (game.phase !== 'discuss') return;
      const reply = generateUniqueReply(aiIdx, contextPriority);
      if (reply) {
        if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
        game.aiUsedLinesGlobal.add(reply);
        aiSay(game, aiIdx, reply);
      }
    }, delay);

    responseIndex++;
  });
}

// Enhanced AI vote with character-specific reasoning
function aiVoteEnhanced(game, pIdx) {
  if (game.phase !== 'vote') return;
  const player = game.players[pIdx];
  if (!player || !player.isAI) return;
  if (game.votes[pIdx] !== undefined) return;

  const charIdx = game.characterAssignments[pIdx];
  const isMurderer = charIdx === 0;

  let voteTarget;
  if (isMurderer) {
    // Murderer votes for the most threatening non-murderer
    const options = game.players
      .map((_, i) => i)
      .filter(i => i !== pIdx && game.characterAssignments[i] !== 0);
    // Prefer voting for 林薇(1) or 张峰(2) — they know the most
    const preferred = options.filter(i => {
      const c = game.characterAssignments[i];
      return c === 1 || c === 2;
    });
    voteTarget = preferred.length > 0 ? pickRandom(preferred) : pickRandom(options);
  } else {
    // 75% chance to vote for the murderer, 25% random
    const murdererIdx = game.characterAssignments.indexOf(0);
    if (Math.random() < 0.75 && murdererIdx !== pIdx) {
      voteTarget = murdererIdx;
    } else {
      const options = game.players
        .map((_, i) => i)
        .filter(i => i !== pIdx);
      voteTarget = pickRandom(options);
    }
  }

  game.votes[pIdx] = voteTarget;

  const voterChar = CHARACTERS[charIdx];
  const targetChar = CHARACTERS[game.characterAssignments[voteTarget]];

  sysMsg(game.roomCode, `🗳️ ${voterChar.name} (AI) 投票给了 ${targetChar.name}`);

  // Dynamically generate vote reasoning
  const targetCharIdxForVote = game.characterAssignments[voteTarget];
  let voteReason = generateAILine(game, charIdx, pIdx, { type: 'vote', targetCharIdx: targetCharIdxForVote });

  // Ensure uniqueness
  const used = game.aiUsedLinesGlobal || new Set();
  if (!voteReason || used.has(voteReason)) {
    // Fallback — generate with clue reference
    const clueKey = pickSharedClueForReference(game, charIdx);
    const clueName = clueKey && ITEMS[clueKey] ? ITEMS[clueKey].name : '现有证据';
    voteReason = `综合${clueName}等线索，我投给${targetChar.name}。`;
    if (used.has(voteReason)) {
      voteReason = `我的判断是${targetChar.name}，他/她最有嫌疑。`;
    }
  }
  // Pre-claim the vote reason
  if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
  game.aiUsedLinesGlobal.add(voteReason);
  aiSay(game, pIdx, voteReason);

  checkAllVoted(game);
}

