// Clear all AI timers
function clearAITimers(game) {
  game.aiTimers.forEach(timer => {
    try { clearInterval(timer); } catch(e) {}
    try { clearTimeout(timer); } catch(e) {}
  });
  game.aiTimers = [];
  game.dmTimers.forEach(timer => {
    try { clearTimeout(timer); } catch(e) {}
  });
  game.dmTimers = [];
  // Also clear per-player takeover timers
  game.players.forEach(p => {
    if (p.takeoverTimers) {
      p.takeoverTimers.forEach(t => { try { clearInterval(t); } catch(e){} try { clearTimeout(t); } catch(e){} });
      p.takeoverTimers = null;
    }
  });
}

// Start AI takeover timers for a single player who left mid-game
function startAITakeover(game, pIdx) {
  const player = game.players[pIdx];
  if (!player) return;
  // Clean up any existing takeover timers
  stopAITakeover(game, pIdx);

  player.takeoverTimers = [];
  const phase = game.phase;

  if (phase === 'explore') {
    const exploreInterval = setInterval(() => {
      aiExplore(game, pIdx);
      aiIdleChat(game, pIdx);
    }, 6000 + Math.random() * 8000);
    player.takeoverTimers.push(exploreInterval);
  } else if (phase === 'discuss' || phase === 'dm_question') {
    const discussInterval = setInterval(() => {
      aiDiscussEnhanced(game, pIdx);
    }, 8000 + Math.random() * 10000);
    player.takeoverTimers.push(discussInterval);
  } else if (phase === 'vote') {
    const delay = 3000 + Math.random() * 15000;
    const voteTimer = setTimeout(() => { aiVoteEnhanced(game, pIdx); }, delay);
    player.takeoverTimers.push(voteTimer);
  }
  // story / character_selection / character_intro / act_transition: no AI action needed
}

// Stop AI takeover timers for a single player (when they rejoin)
function stopAITakeover(game, pIdx) {
  const player = game.players[pIdx];
  if (!player || !player.takeoverTimers) return;
  player.takeoverTimers.forEach(t => { try { clearInterval(t); } catch(e){} try { clearTimeout(t); } catch(e){} });
  player.takeoverTimers = null;
}

// Check and award hidden clues based on chat keywords
function checkHiddenClues(game, pIdx, msg) {
  if (!game.hiddenCluesFound) game.hiddenCluesFound = new Set();
  const currentAct = game.currentAct || 1;

  Object.entries(ITEMS).forEach(([key, item]) => {
    if (!item.hidden) return;
    if (!item.triggerKeyword) return;
    if (item.act && item.act !== currentAct) return;
    if (game.hiddenCluesFound.has(key)) return;

    const lowerMsg = msg.toLowerCase();
    const lowerKeyword = item.triggerKeyword.toLowerCase();
    if (lowerMsg.includes(lowerKeyword)) {
      const player = game.players[pIdx];
      if (player.inventory.includes(key)) return;
      if (game.foundClues.includes(key)) return;

      // Award the hidden clue to this player
      player.inventory.push(key);
      game.foundClues.push(key);
      game.hiddenCluesFound.add(key);
      if (!game.clueFinders) game.clueFinders = {};
      game.clueFinders[key] = pIdx;

      const charIdx = game.characterAssignments[pIdx];
      const charName = CHARACTERS[charIdx].name;
      const charAvatar = CHARACTERS[charIdx].avatar;

      io.to(player.id).emit('hidden-clue-found', { itemKey: key, item });
      sysMsg(game.roomCode, `💫 ${charAvatar} ${charName} 在交流中触发了隐藏线索：${item.name}`);

      // Also add to chat as a DM message
      const dmMsg = {
        id: Date.now() + Math.random(),
        playerId: -1,
        playerName: 'DM',
        characterName: 'DM 主持人',
        avatar: '🎭',
        color: '#d4a017',
        text: `💫 系统提示：${charName} 的发言触发了隐藏线索——"${item.name}"已自动存入其背包。`,
        timestamp: Date.now(),
        type: 'dm'
      };
      game.chatLog.push(dmMsg);
      emitToRoom(game.roomCode, 'dm-message', dmMsg);
    }
  });
}

