  function dmSay(game, text) {
    const msg = {
      id: Date.now() + Math.random(),
      playerId: -1,
      playerName: 'DM',
      characterName: 'DM 主持人',
      avatar: '🎭',
      color: '#d4a017',
      text: text,
      timestamp: Date.now(),
      type: 'dm'
    };
    game.chatLog.push(msg);
    emitToRoom(game.roomCode, 'dm-message', msg);
  }

  function startDMRound(game, actIndex) {
    if (actIndex >= DM_ROUNDS.length) {
      // All acts done -> go to discussion
      return startDiscussionPhase(game);
    }

    const round = DM_ROUNDS[actIndex];
    game.currentAct = actIndex + 1;
    game.phase = 'explore';
    game.endActVotes = new Set(); // Reset act-end votes
    game.dmQuestionActive = false;
    game.dmAnswers = new Set();
    game.dmHintIndex[actIndex] = 0;
    game.cluesThisAct = {}; // Reset per-act clue count for all players

    // When entering a new act, make all clues from previous acts public (shared)
    // Excluding hidden clues and destroyed clues
    if (actIndex > 0) {
      const prevAct = actIndex; // previous act number (1-based)
      const destroyedItems = game.destroyedItems || [];
      const newlyShared = [];
      Object.entries(ITEMS).forEach(([key, item]) => {
        if (item.hidden) return; // skip hidden clues
        if (destroyedItems.includes(key)) return; // skip destroyed clues
        if (!item.act || item.act > prevAct) return; // only previous acts
        // All non-hidden, non-destroyed clues from previous acts become public
        if (!game.sharedClueKeys) game.sharedClueKeys = [];
        if (!game.sharedClueKeys.includes(key)) {
          game.sharedClueKeys.push(key);
          newlyShared.push(key);
        }
      });
      // Notify all players that previous act clues are now public
      if (newlyShared.length > 0) {
        sysMsg(game.roomCode, `📢 前一幕的所有线索已自动公开（共${newlyShared.length}条），请查看线索区。`);
        // Emit clue-shared events for each newly public clue
        newlyShared.forEach(clueKey => {
          const item = ITEMS[clueKey];
          if (item) {
            emitToRoom(game.roomCode, 'clue-shared', {
              fromCharacter: '系统',
              fromAvatar: '📢',
              itemKey: clueKey,
              item: { name: item.name, desc: item.desc }
            });
          }
        });
      }
    }

    game.timerEnd = Date.now() + ROUND_TIME * 1000;

    emitGameState(game.roomCode);
    emitToRoom(game.roomCode, 'act-start', { act: round.act, title: round.title });

    // Stagger DM intro messages
    round.dmIntro.forEach((line, i) => {
      const timer = setTimeout(() => {
        if (game.currentAct === actIndex + 1) {
          dmSay(game, line);
        }
      }, (i + 1) * 2500);
      game.dmTimers.push(timer);
    });

    // Periodic DM hints (at 40% and 70% of round time)
    const hint1Time = ROUND_TIME * 0.4 * 1000;
    const hint2Time = ROUND_TIME * 0.7 * 1000;
    if (round.dmHints.length > 0) {
      const h1 = setTimeout(() => {
        if (game.currentAct === actIndex + 1 && !game.dmQuestionActive) {
          dmSay(game, '💡 ' + round.dmHints[0]);
        }
      }, hint1Time);
      game.dmTimers.push(h1);
    }
    if (round.dmHints.length > 1) {
      const h2 = setTimeout(() => {
        if (game.currentAct === actIndex + 1 && !game.dmQuestionActive) {
          dmSay(game, '💡 ' + round.dmHints[1]);
        }
      }, hint2Time);
      game.dmTimers.push(h2);
    }

    // Auto-end round when timer expires
    if (game.timer) clearTimeout(game.timer);
    game.timer = setTimeout(() => {
      if (game.currentAct === actIndex + 1 && game.phase === 'explore') {
        endDMRound(game, actIndex);
      }
    }, ROUND_TIME * 1000);

    // AI explore for this round
    if (hasAIPlayers(game)) {
      clearAITimers(game);
      startAIExploration(game);
    }
  }

  function endDMRound(game, actIndex) {
    const round = DM_ROUNDS[actIndex];
    game.phase = 'dm_question';
    game.dmQuestionActive = true;
    game.dmAnswers = new Set();
    if (game.timer) clearTimeout(game.timer);

    // Set 3-minute timer for DM question phase
    game.timerEnd = Date.now() + 180 * 1000;

    emitToRoom(game.roomCode, 'dm-question', {
      question: round.question,
      act: round.act
    });
    dmSay(game, round.question);
    emitGameState(game.roomCode);

    // DM also asks in chat for visibility
    sysMsg(game.roomCode, '🎭 DM提出了问题，请在聊天框中输入你们的答案。');

    // Send 3-minute countdown notice to all players
    emitToRoom(game.roomCode, 'dm-time-limit', { seconds: 180 });

    // Auto-advance after 3 minutes even if not enough answers
    const autoTimer = setTimeout(() => {
      if (game.dmQuestionActive) {
        advanceFromQuestion(game, actIndex);
      }
    }, 180000);
    game.dmTimers.push(autoTimer);

    // Force all AI players to answer the DM question — each exactly once, no repeats
    if (hasAIPlayers(game)) {
      const aiPlayers = game.players
        .map((p, i) => ({ p, i }))
        .filter(({ p }) => p.isAI && p.connected)
        .map(({ i }) => i);

      aiPlayers.forEach((aiIdx, idx) => {
        const delay = 3000 + idx * (2500 + Math.random() * 2000);
        const timer = setTimeout(() => {
          if (game.phase !== 'dm_question') return;
          const reply = generateDMAnswer(game, aiIdx, round);
          if (reply) {
            // Ensure global uniqueness
            if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
            if (!game.aiUsedLinesGlobal.has(reply)) {
              game.aiUsedLinesGlobal.add(reply);
              aiSay(game, aiIdx, reply);
            }
          }
        }, delay);
        game.dmTimers.push(timer);
      });
    }
  }

  // Generate an AI character's answer to the DM question
  function generateDMAnswer(game, aiIdx, round) {
    const charIdx = game.characterAssignments[aiIdx];
    const isMurderer = charIdx === 0;
    const knowledge = CHAR_KNOWLEDGE[charIdx];
    const character = CHARACTERS[charIdx];
    const sharedClues = game.sharedClueKeys || [];
    const used = game.aiUsedLinesGlobal || new Set();

    // Build a pool of candidate answers, try each until we find a unique one

    // 1. Character-specific DM answer based on act and what they know
    const actSpecific = generateActSpecificDMAnswer(game, charIdx, isMurderer, round);
    if (actSpecific && !used.has(actSpecific)) return actSpecific;

    // 2. Reference a shared clue the character knows about
    const knownClues = sharedClues.filter(k => knowledge.knowsAbout.includes(k));
    for (const clueKey of knownClues) {
      const item = ITEMS[clueKey];
      if (!item) continue;
      const reactions = [
        `${item.name}是我发现的，${item.desc.substring(0, 30)}...这很关键。`,
        `关于${item.name}，我认为这直接关系到案件的真相。`,
        `我注意到${item.name}——这绝对不是巧合。`
      ];
      for (const r of reactions) {
        if (!used.has(r)) return r;
      }
    }

    // 3. Reference any shared clue
    for (const clueKey of sharedClues) {
      const item = ITEMS[clueKey];
      if (!item) continue;
      const reactions = [
        `${item.name}的存在说明有人在说谎。`,
        `大家都看到了${item.name}，这怎么解释？`,
        `我认为${item.name}是最重要的证据之一。`
      ];
      for (const r of reactions) {
        if (!used.has(r)) return r;
      }
    }

    // 4. General observation about the question
    const observations = [
      `关于DM的问题，我认为我们需要从动机、手段和时机三方面分析。`,
      `这个问题的答案需要结合我们目前发现的所有线索来推断。`,
      `我的看法可能和大家不同，但证据会说明一切。`,
      `我们不能仅凭一两个线索下结论，需要综合考虑。`,
      `每个线索都有它的意义，关键在于如何把它们串联起来。`
    ];
    for (const obs of observations) {
      if (!used.has(obs)) return obs;
    }

    // 5. Alibi-based answer
    const alibiAnswers = [
      `从我的角度看，${knowledge.alibi}`,
      `说到时间线，${knowledge.alibi}`
    ];
    for (const a of alibiAnswers) {
      if (!used.has(a)) return a;
    }

    return null;
  }

