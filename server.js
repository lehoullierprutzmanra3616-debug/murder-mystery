// AI responds to a human player's chat message
function aiRespondToChat(game, humanMsg, humanCharIdx) {
  const msg = humanMsg.toLowerCase();
  const eligibleAI = game.players
    .map((p, i) => ({ p, i }))
    .filter(({ p, i }) => p.isAI && game.phase !== 'vote')
    .map(({ i }) => i);

  if (eligibleAI.length === 0) return;

  // --- Check if a specific character name is mentioned ---
  const mentionedAI = [];
  for (const aiIdx of eligibleAI) {
    const charIdx = game.characterAssignments[aiIdx];
    const charName = CHARACTERS[charIdx].name;
    if (humanMsg.includes(charName)) {
      mentionedAI.push(aiIdx);
    }
  }

  // --- Determine keyword context (if any) ---
  const keywordMap = [
    { keywords: ['凶手是谁', '谁杀', '谁是凶手', '谁干的', '谁杀了'], context: 'accuse' },
    { keywords: ['曹博士', '曹文轩'], context: 'accuse' },
    { keywords: ['红酒', '毒药', '氰化钾', '下毒', '中毒'], context: 'react' },
    { keywords: ['实验', '人体实验', '研究'], context: 'react' },
    { keywords: ['陈默', '保安', '死者'], context: 'react' },
    { keywords: ['停电', '监控', '门禁'], context: 'react' },
    { keywords: ['鞋印', '锤子', '指纹', '物证'], context: 'react' },
    { keywords: ['勒索', '敲诈'], context: 'react' },
    { keywords: ['0315', '密码', '保险箱'], context: 'react' },
    { keywords: ['密室', '认罪书', '日记'], context: 'react' }
  ];

  let matchedKeyword = null;
  for (const km of keywordMap) {
    if (km.keywords.some(kw => msg.includes(kw.toLowerCase()))) {
      matchedKeyword = km;
      break;
    }
  }

  // Helper: generate a unique reply for an AI, trying multiple strategies
  function generateUniqueReply(aiIdx, contextTypes) {
    const charIdx = game.characterAssignments[aiIdx];
    const isMurderer = charIdx === 0;
    const used = game.aiUsedLinesGlobal || new Set();

    // Try each context type in order until we get a unique line
    for (const ctxType of contextTypes) {
      let line = null;

      if (ctxType === 'contextual') {
        line = generateContextualResponse(game, humanMsg, charIdx, aiIdx, isMurderer);
      } else {
        line = generateAILine(game, charIdx, aiIdx, { type: ctxType });
      }

      if (line && !used.has(line)) {
        return line;
      }
    }

    // Last resort: try generating a reaction with different clue references
    const sharedClues = game.sharedClueKeys || [];
    if (sharedClues.length > 0) {
      // Try each shared clue as a basis for a unique reaction
      for (const clueKey of sharedClues) {
        const line = generateClueReaction(game, charIdx, clueKey, isMurderer);
        if (line && !used.has(line)) {
          return line;
        }
      }
    }

    // Try generating an alibi with variation
    const alibiLine = generateAlibi(charIdx);
    if (alibiLine && !used.has(alibiLine)) {
      return alibiLine;
    }

    // Try a discussion observation
    const obsLine = generateDiscussionObservation(game, charIdx, isMurderer);
    if (obsLine && !used.has(obsLine)) {
      return obsLine;
    }

    return null;
  }

  // --- Build the responder list ---
  // ALL eligible AI will respond, with mentioned AI first
  const orderedResponders = [];

  // Mentioned AI first
  for (const aiIdx of mentionedAI) {
    orderedResponders.push(aiIdx);
  }

  // Then all other eligible AI
  for (const aiIdx of eligibleAI) {
    if (!orderedResponders.includes(aiIdx)) {
      orderedResponders.push(aiIdx);
    }
  }

  // Determine context type priority based on keyword match
  let contextPriority;
  if (matchedKeyword) {
    contextPriority = ['contextual', matchedKeyword.context, 'react', 'discuss', 'idle'];
  } else {
    contextPriority = ['contextual', 'react', 'discuss', 'idle', 'accuse'];
  }

  // Generate unique replies for all responders
  const finalResponders = [];
  for (const aiIdx of orderedResponders) {
    const reply = generateUniqueReply(aiIdx, contextPriority);
    if (reply) {
      // Pre-claim immediately
      if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
      game.aiUsedLinesGlobal.add(reply);
      finalResponders.push({ responderIdx: aiIdx, reply });
    }
  }

  // Schedule each response with increasing delay
  finalResponders.forEach(({ responderIdx, reply }, idx) => {
    const delay = (idx + 1) * (2000 + Math.random() * 2000);
    setTimeout(() => {
      if (game.phase === 'vote') return;
      aiSay(game, responderIdx, reply);
    }, delay);
  });
}

