// Check if a discussion line is available (its required clues have been shared)
function isLineAvailable(game, line) {
  const shared = game.sharedClueKeys || [];

  // 1. Exact match in DISCUSS_LINE_REQUIRES
  const required = DISCUSS_LINE_REQUIRES[line];
  if (required) {
    return required.some(key => shared.includes(key));
  }

  // 2. Pattern match for dynamic strings
  for (const [pattern, clueKeys] of LINE_PATTERN_REQUIRES) {
    if (line.includes(pattern)) {
      if (!clueKeys.some(key => shared.includes(key))) {
        return false;
      }
    }
  }

  return true; // No requirement matched — always available
}

// Filter a discussion pool to only lines whose required clues have been shared
function filterAvailableLines(game, lines) {
  if (!lines || lines.length === 0) return [];
  const available = lines.filter(l => isLineAvailable(game, l));
  return available.length > 0 ? available : [];
}

