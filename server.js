// Get uncollected items in a room
function getUncollectedItemsInRoom(game, roomKey, pIdx) {
  const player = game.players[pIdx];
  const currentAct = game.currentAct || 1;
  return Object.entries(ITEMS)
    .filter(([key, item]) =>
      item.location === roomKey &&
      !player.inventory.includes(key) &&
      !game.foundClues.includes(key) && // Skip clues already found by anyone (shared pool)
      !item.hidden && // Skip hidden clues (they trigger via chat)
      (!item.act || item.act <= currentAct)
    )
    .map(([key, item]) => ({ key, ...item }));
}

// AI explores: move to random connected room, maybe find an item
function aiExplore(game, pIdx) {
  if (game.phase !== 'explore') return;
  const player = game.players[pIdx];
  if (!player || !player.isAI) return;

  const rooms = getAIAccessibleRooms(game, pIdx);
  if (rooms.length === 0) return;

  const targetRoom = pickRandom(rooms);
  const oldRoom = player.currentRoom;

  if (targetRoom !== oldRoom) {
    // Move AI
    const prevRoom = player.currentRoom;
    player.currentRoom = targetRoom;

    // Notify the human player about the AI's room change
    const charIdx = game.characterAssignments[pIdx];
    const charName = CHARACTERS[charIdx].name;
    const charAvatar = CHARACTERS[charIdx].avatar;

    // Check if human is in target room
    const humanIdx = getHumanIndex(game);
    if (humanIdx >= 0 && game.players[humanIdx].currentRoom === targetRoom) {
      io.to(game.players[humanIdx].id).emit('player-enter-room', {
        character: charName,
        avatar: charAvatar
      });
    }
    // Check if human is in old room
    if (humanIdx >= 0 && game.players[humanIdx].currentRoom === prevRoom) {
      io.to(game.players[humanIdx].id).emit('player-leave-room', {
        character: charName
      });
    }
  }

  // Maybe find an item (40% chance)
  if (Math.random() < 0.4) {
    const items = getUncollectedItemsInRoom(game, game.players[pIdx].currentRoom, pIdx);
    if (items.length > 0) {
      const item = pickRandom(items);
      // Skip puzzle items for AI (they can't solve puzzles)
      if (item.requiresPuzzle) return;

      // Check clue limit for AI — 3 per act, hidden clues excluded
      const maxClues = 3;
      if (!game.cluesThisAct) game.cluesThisAct = {};
      const aiActClueCount = game.cluesThisAct[pIdx] || 0;
      // Secondary check: count non-hidden, non-puzzle clues in inventory for this act
      const actClueItemsInInv = player.inventory.filter(k => {
        const it = ITEMS[k];
        return it && !it.hidden && !it.requiresPuzzle && !it.keyItem && !k.endsWith('_door') &&
               (!it.act || it.act === game.currentAct);
      }).length;
      if (aiActClueCount >= maxClues || actClueItemsInInv >= maxClues) return;

      player.inventory.push(item.key);
      game.foundClues.push(item.key);
      if (!game.clueFinders) game.clueFinders = {};
      game.clueFinders[item.key] = pIdx;
      // Increment per-act clue count (use max of both counters to stay in sync)
      game.cluesThisAct[pIdx] = Math.max(aiActClueCount, actClueItemsInInv) + 1;

      const charIdx = game.characterAssignments[pIdx];
      const charName = CHARACTERS[charIdx].name;
      const charAvatar = CHARACTERS[charIdx].avatar;

      // Tell human (no details)
      const humanIdx = getHumanIndex(game);
      if (humanIdx >= 0) {
        io.to(game.players[humanIdx].id).emit('system-message', {
          text: `🤖 ${charAvatar} ${charName} 似乎发现了什么...`
        });
      }

      // Broadcast updated game state so all clients see the clue as claimed
      emitGameState(game.roomCode);

      // Sometimes share with everyone (60% chance)
      if (Math.random() < 0.6) {
        emitToRoom(game.roomCode, 'clue-shared', {
          fromCharacter: charName + ' (AI)',
          fromAvatar: charAvatar,
          itemKey: item.key,
          item: { name: item.name, desc: item.desc }
        });
        if (!game.sharedClueKeys) game.sharedClueKeys = [];
        if (!game.sharedClueKeys.includes(item.key)) game.sharedClueKeys.push(item.key);
        sysMsg(game.roomCode, `📜 ${charName} 向所有人展示了：${item.name}`);

        // Dynamically generate clue share dialogue — only said when the clue is publicly shared
        const line = generateAILine(game, charIdx, pIdx, { type: 'clue_share', clueKey: item.key });
        if (line) {
          const used = game.aiUsedLinesGlobal || new Set();
          if (!used.has(line)) {
            if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
            game.aiUsedLinesGlobal.add(line);
            aiSay(game, pIdx, line);
          }
        }
      }
      // When not sharing: AI stays silent about the clue — no clue-specific dialogue
    }
  }
}

// AI sends a chat message
function aiSay(game, pIdx, text) {
  const player = game.players[pIdx];
  const charIdx = game.characterAssignments[pIdx];
  const character = CHARACTERS[charIdx];
  const message = {
    id: Date.now() + Math.random(),
    playerId: pIdx,
    playerName: player.name + ' (AI)',
    characterName: character.name,
    avatar: character.avatar,
    color: character.color,
    text: text,
    timestamp: Date.now(),
    type: 'chat'
  };
  game.chatLog.push(message);
  // Track used lines globally — no AI (including self) will repeat a line already said
  if (!game.aiUsedLinesGlobal) game.aiUsedLinesGlobal = new Set();
  game.aiUsedLinesGlobal.add(text);
  emitToRoom(game.roomCode, 'chat-message', message);
}

// Pick a random line from an array, avoiding lines already said by ANY AI player.
// Returns null if all lines have been used (caller should handle by staying silent).
function pickUnusedLine(game, pIdx, lines) {
  if (!lines || lines.length === 0) return null;
  const used = game.aiUsedLinesGlobal || new Set();
  const available = lines.filter(l => !used.has(l));
  if (available.length > 0) {
    return available[Math.floor(Math.random() * available.length)];
  }
  // All lines in this pool have been used — return null so caller stays silent
  return null;
}

